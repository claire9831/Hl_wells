function velvsxplot(allvel,x,xbnds,color)

minx = min(x);
maxx = max(x(~isinf(x)));
if nargin <3,
    N_bins = 31;
    xbnds = linspace(0.9*minx,1.1*maxx,N_bins+1);
elseif length(xbnds) >1,
    N_bins = length(xbnds)-1;
else
    N_bins = xbnds;
    xbnds = linspace(0.9*minx,1.1*maxx,N_bins+1);
end;
[velcounts,whichbin] = histc(x,xbnds);
xmid = 0.5*(xbnds(1:end-1)+xbnds(2:end));

avvel = NaN(N_bins,1);
stdvel = NaN(N_bins,1);

for i_bin = 1:N_bins;
    if velcounts(i_bin) ~= 0,
        avvel(i_bin) = mean(allvel(whichbin==i_bin));
        stdvel(i_bin) = std(allvel(whichbin==i_bin))/sqrt(velcounts(i_bin));
    end;
end;

is_nonuclinbin = isnan(avvel);
xmid(is_nonuclinbin) = [];
avvel(is_nonuclinbin) = [];
stdvel(is_nonuclinbin) = [];

hold on;
h = errorbar(xmid,avvel,stdvel,color,'LineWidth',2);
set(h,'markerfacecolor',get(h,'color'));

return;


